let levelA1 = document.getElementById("levelA1");
let letter = document.getElementById("letter");
levelA1.onmouseover = () => {
  levelA1.style.backgroundColor = "rgb(195, 65, 87)";
  letter.style.color = "white";
  levelA1.onmouseout = () => {
    levelA1.style.backgroundColor = "rgb(32, 31, 31)";
    letter.style.color = "rgb(195, 65, 87)";
  };
};

